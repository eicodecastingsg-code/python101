# To iterate a List (to sequentially access items in a list)
# for-each loop
# range-based for loop

x = [10, 20, 30, 40, 50, 60]
for num in x:
    print(num)

print()


z = [1, 2, 3, "grow", "a", "garden"]
for item in z:
    print(item)

print()


# Iterate a list with range-based for loop
y = [5, 10, 15, 20, 25, 30]
for i in range(0, 6, 1):
    print(y[i])






































